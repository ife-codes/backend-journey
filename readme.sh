# Backend Journey – REST API Experiments

Welcome! This repo documents my hands-on learning journey through backend development using Node.js and Express.

Each folder in the `projects/` directory is a self-contained mini API or feature experiment — from basic CRUD to advanced auth and database integrations.

This is where I break things, rebuild them, and grow as a backend engineer.

## Why this repo?

To:
- Practice building APIs from scratch
- Learn how real-world backend systems work
- Try out new tools like PostgreSQL, Supabase, Redis, and more
- Track my progress and share what I learn

Stay tuned — lots of cool experiments ahead!
